package com.springzero.factory;
/**
 * @version 创建时间：2016年1月6日 下午10:12:36
 * 类说明
 */
public class Car {
	private String name;
	private int price;
	
	public Car(){}
	
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
}
